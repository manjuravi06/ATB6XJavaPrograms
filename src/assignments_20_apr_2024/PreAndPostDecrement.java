package assignments_20_apr_2024;

public class PreAndPostDecrement {

    public static void main(String[] args) {

        int b = 10;

        System.out.println(--b + b++ + ++b);

        /*
        First operation : result = 9 , value of a = 9;
        Second operation : result = 9 , value of a = 10;
        Third operation : result = 11 , value of a = 11;
        Final value of result : 9+9+11 = 29;
         */

    }

}
